def _gemini_generate_image_bytes(prompt: str) -> bytes:
    """
    Returns raw image bytes generated by Gemini.
    Requires: pip install google-genai
    Env var: GEMINI_API_KEY (or GOOGLE_API_KEY for compatibility)
    """
    from google import genai
    from google.genai import types

    api_key = os.environ.get("GEMINI_API_KEY") or os.environ.get("GOOGLE_API_KEY")
    if not api_key: raise RuntimeError("GEMINI_API_KEY is not set.")
    
    client = genai.Client(api_key=api_key)
    
    resp = client.models.generate_content(
        model="gemini-3.1-flash-image",
        contents=prompt,
        config=types.GenerateContentConfig(
            response_modalities=["IMAGE"],
            safety_settings=[types.SafetySetting(category="HARM_CATEGORY_DANGEROUS_CONTENT", threshold="BLOCK_ONLY_HIGH")],
        ),
    )
    
    parts = getattr(resp, "parts", None)
    
    if not parts and getattr(resp, "candidates", None):
        try: parts = resp.candidates[0].content.parts
        except Exception: parts = None
    
    if not parts: raise RuntimeError("No image content returned.")
    
    for part in parts:
        inline = getattr(part, "inline_data", None)
        if inline and getattr(inline, "data", None):
            return inline.data
    raise RuntimeError("No inline image bytes found.")

def _safe_slug(title: str) -> str:
    s = title.strip().lower()
    s = re.sub(r"[^a-z0-9 _-]+", "", s)
    s = re.sub(r"\s+", "_", s).strip("_")
    return s or "blog"

def generate_and_place_images(state: State) -> dict:
    plan = state["plan"]
    assert plan is not None

    md = state.get("md_with_placeholders") or state["merged_md"]
    image_specs = state.get("image_specs", []) or []

    images_dir = Path("images")
    images_dir.mkdir(exist_ok=True)

    for spec in image_specs:
        placeholder = spec["placeholder"]
        filename = spec["filename"]
        out_path = images_dir / filename
        if not out_path.exists():
            try:
                img_bytes = _gemini_generate_image_bytes(spec["prompt"])
                out_path.write_bytes(img_bytes)
            except Exception as e:
                md = md.replace(placeholder, f"> **[IMAGE FAILED]** {e}\n")
                continue
        md = md.replace(placeholder, f"![{spec['alt']}](images/{filename})\n*{spec['caption']}*")

    filename = f"{_safe_slug(plan.blog_title)}.md"
    Path(filename).write_text(md, encoding="utf-8")
    
    # Saving Final markdown to state
    state["final"] = md 
    
    # Saving to Supabase (if configured)
    if supabase:
        try:
            def custom_serializer(obj):
                if hasattr(obj, "model_dump"): return obj.model_dump()
                from datetime import date, timedelta
                if isinstance(obj, (date, timedelta)): return str(obj)
                return str(obj)
            
              # state to JSON string 
            state_json = json.loads(json.dumps(state, default=custom_serializer))
            supabase.table("blogs").insert({
                "title": plan.blog_title,
                "content": md,
                "full_state": state_json
            }).execute()
            print(f"✅ Successfully saved '{plan.blog_title}' to Supabase!")
        except Exception as e:
            print(f"❌ Error saving to Supabase: {e}")

    return {"final": md}

